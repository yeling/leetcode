    def sampleStats(self, count: List[int]) -> List[float]:  
        
        n = len(count)
        minimum = -1
        maximum = -1
        
        s = 0
        mode = 0
        most = 0
        #(value, index)
        preCnt = [0]*(n+1)

        for i in range(n):
            if count[i] > 0 and minimum == -1:
                minimum = i
            if count[-1-i] > 0 and maximum == -1:
                maximum = n - 1 - i
            preCnt[i + 1] = preCnt[i] + count[i]
            s += count[i] * i
            if count[i] > most:
                most = count[i]
                mode = i
        
        mean = s / preCnt[n]
        median = -1
        if preCnt[n]%2 == 1:
            dst = preCnt[n]//2
            cnt = 0
            for i in range(n):
                cnt += count[i]
                if cnt - 1 >= dst:
                    median = i
                    break
        else:
            first = preCnt[n]//2 - 1
            second = preCnt[n]//2
            cnt = 0
            for i in range(n):
                cnt += count[i]
                if cnt - 1 >= first and median == -1:
                    median = i
                if cnt - 1 >= second:
                    median = (median +  i)/2
                    break


        return [minimum, maximum, mean, median, mode]
    
